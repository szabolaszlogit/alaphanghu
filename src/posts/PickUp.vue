    <h1>A hangszedő</h1>
<p>Az elektromos gitár legfontosabb alkatrésze a hangszedő (angolul pickup), ez alakítja át a húrok rezgését elektromos jelekké. Elektromos indukció elvén működik, a húrok mágneses térben rezegnek, így a mágnes körülivevő tekercsben feszültség keletkezik, ez jut el a többi eszközön keresztül a hangfalakba, ahol megszólal a hang.</p>

<h2>Single coil pickup</h2>
<p>Az egytekercses hangszedő csilingelőbb, fényesebb hanggal rendelkezik. Ide tartozik a tipikus Fender Stratocaster hang. Alacsonyabb a kimeneti jelszintjük (ez a hangosításban játszik szerepet), hamarabb gerjednek, hajlamosabbak a zúgásra. Viszont lelke van :)</p>
