    <h1>Effektek</h1>
<p>Már maga a gitár vagy az erősítő is számos hangképet tud előállítani, de ha nem lenne elég, effektek segítségével még változatosabb hangzásokat hozhatunk létre. Effekteket nem csak a gitáron, hanem más hangszereken, magán az énekhangon is alkalmaznak.</p>

<p>Az effektek lehetnek analógok, digitálisak, vagy mechanikusak, bár igazából a hangzás szempontjából ez annyira nem fontos. Rengeteg effekt létezik, és az egyes effekteket természetesen lehet szabályozni, a tulajdonságait állítgatni. Az effekteket többnyire pedál formában használjuk, így játék közben tudjuk őket a lábunkkal szabályozni, de vannak rackbe szerelhető effektek is. Az effekteket köthetjük a gitár és az erősítő közé, illetve az erősítő után is, ám az, hogy milyen sorrendben használjuk az effekteket alapvetően meghatározza a hangzást, például egy visszhang nem mindegy, hogy magára a száraz gitárhangra van hatással, vagy a már feleffektezett, erősítőből kilépő hangra.</p>

<p>Vannak olyan művészek, akik kevés effektet használnak, és annak, akik beborítják a pedálokkal a színpadot. Az alábbiakban bemutatok néhány effektet.</p>

<h2>Torzítók (Distortion, Overdrive, Fuzz)</h2>
<p>A rockzene, metálzene legismertebb effektjei, jellegzetes hangzásunkat mindenki felismeri. Köztük a különbség a torzítás módjában, jellegében van. A bejövő jelet az effekt levágja, sok felharmonikus hang jelenik meg. Ide sorolhatjuk az erősítő saját torzítását (gain, volume) is.</p>

<h2>Compressor</h2>
<p>A halkabb részeket hangosítja, a hangosabbakat halkítja, tehát egyenletesebb hangerőt kapunk. Ritmusgitározásnál szokták használni, egyenletesebb lesz a kíséret hangja. Túl sok kompresszor azonban élettelenné, dinamikátlanná teheti a hangot.</p>

<h2>Limiter</h2>
<p>Adott hangerő fölött levágja a hangot. Hasonlít a kompresszorhoz, de a halkabb hangoknál a dinamika tud érvényesülni.</p>

<h2>Noise gate (zajzár)</h2>
<p>A zajokat vágja le, túlvezérlése esetén a zenéből is lecsíp, ez különösen a hang elindulásakor, megállásakor figyelhető meg. A nem oda tartozó zúgást, sistergést tudjuk vele kiszűrni, ha szükséges.</p>

<h2>Equalizer</h2>
<p>Hangszínszabályzó. Megadott frekvenciákat, azaz hangmagasságokat erősíthetünk vagy csillapíthatunk. Gitárnál szokták használni a mély hangok levágására (az a basszusgitárhoz tartozik), a közép kiemelésére szólózáskor. A sípolást, bántó magas hangokat a magasabb frekvenciák tompításával lehet kiszűrni.</p>

<h2>Wah</h2>
<p>Nagyon jellegzetes effekt. Hápogó, hullámzó, változó hangot eredményez, amit a gitáros a pedál segítségével tud szabályozni. A Touch-Wah a hangerőtől függően alkalmazza az effektet a hangra.</p>

<h2>Choir (kórus)</h2>
<p>A bejövő hanghoz hozzáadja a korábban beérkezett hangot, néhány századmásodperc késéssel. Egy kórus énekléséhez hasonló hatást kapunk, ahol az énekeseknek kicsit más a hangszíne, nem egyszerre lépnek be, ezért elmosódottabb, lágyabb lesz a hangkép</p>

<h2>Delay (Visszhang)</h2>
<p>Hasonlóan működik, mint a kórus, de nagyobb késleltetéssel, ami akár 1 másodperc is lehet. A késleltetés mérteke többnyire a zene üteméhez igazodik.</p>

<h2>Zengető (Reverb)</h2>
<p>Hasonlóan működik, mint a kórus, de a késleltetett hang véletlenszerűbb, késése 0,1 másodperc alatt van általában. Leginkább környezetet szimulál. Normál körülmények között nem hallunk természetes effektek nélküli hangot, valamilyen akusztikai környezetben mindig benne vagyunk, és ez hat a hangra is. Ezek nélkül a hang idegennek, sterilnek hat. A reverb képes szimulálni különböző akusztikai feltételeket: szoba, terem, stúdió stb.</p>

<h2>Echo</h2>
<p>Az echo a reverb és a delay effektek tulajdonságait egyesíti: akusztikai környezetet teremt, és a hangok ismétlését is megoldja.</p>

<h2>Pitch Shifter, Octaver</h2>
<p>Az eredeti hang hangmagasságát fel illetve le változtatják, és hozzákeverik az eredetihez. Az octaver egy oktávval magasabb, vagy mélyebb hangokat állít elő.</p>

<p class="info">A fentieken kívül számos más effekt létezik, és minden effektet testre lehet szabni, állítgatni. Leggyakrabban torzítót, zengetőt, visszhangot használunk, esetleg zajszűréssel, hangszínszabályzással. Általában az effekteknél is érvényesül az aranyszabály, hogy a kevesebb néha több.</p>
