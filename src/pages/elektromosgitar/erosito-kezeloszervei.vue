               <h2>Az erősítő kezelőszervei</h2>   
<p>Ki és bekapcsoló gomb minden erősítőn van. Mielőtt bekapcsoljuk, a gitár már legyen csatlakoztatva, a hangerő pedig levéve nullára. Hangerőszabályzó, két – három hangszínszabályzó minden erősítőn van. Középállásból induljunk ki a hangszíneknél, és óvatosan állítgatva keressük meg a nekünk megfelelőt. Sok erősítő rendelkezik Presence, vagy hasonló kapcsolóval, ez is egyfajta hangszínszabályzó, bizonyos hangtartományokat kiemelhetünk vele.</p>
<h2>Gain, Drive, Pre-Gain</h2>
<p>Ezek a fogalmak ugyanazt takarják. Az egyes erősítőkön másképp nevezik az úgynevezett előerősítő (preamp) hangerejét.</p>

<p>A volume (main volume, master) a végfokot (power amp) vezérli, ez adja az erősítő hagyományos értelemben vett hangerejét.</p>
<p>A gain az előfokra van hatással, de nem a hangerő változtatása a cél elsősorban, hanem a hang karakterének megváltoztatása (persze a hangerőre is hatással van). Tulajdonképpen túlvezéreljük a jelet, így a hang torzított, zúzósabb lesz. Egy hangerőszabályzós erősítőkön a hangerő növelésével együtt torzul a hang, tehát vagány torzítást csak nagy hangerőn lehet elérni. A külön gain szabályzó lehetővé teszi, hogy már viszonylag kicsi hangerőn (volume) is lehessen torzított, harapós hangot használni.</p>

<h2>Csatornák</h2>
<p>Számos erősítőn váltogathatunk előre beállított hangzások között, ezeket hívjuk csatornáknak. Legegyszerűbb esetben van egy tiszta és egy rockosabb, metálosabb hangzás (clean, drive), de sok erősítő rendelkezik 3 - 4 csatornával is, és az egyes csatornákon belül is lehetnek alcsatornák, az egyes csatornákon be vagy kikapcsolhatunk hangszínszabályzókat, és így tovább. Természetesen mindent testre szabhatunk, akár zenélés közben is, hiszen az egyes csatornák rendelkeznek külön kezelőgombokkal, szabályzókkal.</p>

