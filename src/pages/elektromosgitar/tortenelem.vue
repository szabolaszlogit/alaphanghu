A Gibson ES-150 1936-ban, a Fender Telecaster 1950-ben (más hangszerekkel, cégekkel együtt) zenei forradalmat indítottak el.              </p>
              <p>A gitár azelőtt is kedvelt és népszerű hangszer volt, de kisebb hangereje miatt nagyzenekarokban, nagyobb helyiségekben nem tudott igazán kibontakozni. A gitárhang elektromos felerősítésével viszont előtérbe került, meghatározó hangszerré lépett elő, halk kísérőhangszerből főszereplő lett, kibontakozhatott a hangszer csodálatos szólóképessége is.
<br>
Az ötvenes évektől az elektromos gitár évtizedekig meghatározó szereplője volt a zenének. Korszakok és zenei stílusok köthetők a hangszerhez, generációk számára jelentette a gitárhang a zenei élményt. Hatása túlmutat önmagán, társadalmi, popkulturális jelenséggé vált. Nemcsak a zenészek, hanem a gitárok is ikonná váltak. Nem sok olyan hangszer van, amelyik ilyen rövid idő alatt ilyen sokat fejlődött. Mérnökök és zenészek alkotómunkájának eredményeképpen egy csodálatos hangszer született, sokszínű és kreatív felhasználási lehetőségekkel. Sok zenei stílusban vált alapvetővé, számos együttes zenéje alapul a gitár hangzásán. Megjelentek a dobon kívül csak gitárt használó zenekarok is, hiszen a gitár alkalmas kíséret játszására, szólózásra és a basszus megszólaltatására is.
<br>
A kétezres években is gyakori a gitárhang a könnyűzenében, a legváltozatosabb műfajokban hallhatunk gitáralapokat, szólókat.
<br>
A gitár kihangosításával már az 1920-as években is kísérleteztek, de az első igazi áttörés a Gibson ES-150 volt. Itt még üreges testre szerelték rá az elektronikát. Ez a gitár nagy sikert aratott a zenészek köreiben, azonban az üreges testnek voltak hátrányai: a hangszer hajlomos volt a gerjedésre (azaz sípolt, zúgott). Leo Fender és Les Paul kezdtek kísérletezni a tömör testtel. A Fender Telecaster volt az első sorozatban gyártott tömör testű elektromos gitár, az 1950-es évektől kezdődően a mai napig töretlen a népszerűsége, a Gibson cég pedig 1952-ben, Les Paul tervei alapján elkészíttette a Les Paul Gold Top gitárt. Ezután kezdődött el az elektromos gitárok aranykora.</p>

