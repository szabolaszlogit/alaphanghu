<h2>Aktív pickup</h2>
<p>Lehet egy, illetve ikertekercses is. A lényegük, hogy valamilyen aktív elektronika (egy előerősítő) is van beléjük építve. Elem szükséges a működésükhöz. Az alacsony jelszíntű hangszedőt az elektronika felerősíti, így, ha kell megmarad a tiszta hang is szépnek, viszont erőteljes torzított hangot is el lehet érni.</p>
<h2>Piezo pickup</h2>
<p>Ezt a típust az akusztikus gitároknál használják, a gitártest rezgését alakítja át elektromos jelekké. Fontos a megfelelő hangszín szabályzás, mert a hang így gyakran éles, steril.</p>
<h2>Milyen hangszedőt használjak?</h2>
<p>Vannak általános trendek, de nincsenek kőbe vésett szabályok. A humbuckereket, aktív hangszedőket általában zúzósabb, metálosabb zenészek használják, a single coilokat gyakrabban a rock, blues zenészek kedvelik. Jellegzetes single coil (és Fender gitár) használók többek között Hendrix, Clapton, a Dire Staits, Stevie Ray Vaughan, David Gilmour (Pink Floyd).

Humbuckert (tipikusan Gibsonnal) használnak többnyire Slash, Led Zeppelin, B. B. King, Bob Marley. Aktív hangszedővel (EMG) zenélnek a Metallica gitárosai

A fenti nevek csak példák, hiszen ők is használnak többféle gitárt, többféle hangszedőt, stúdióban és koncerteken is. Mindenféle stílusban találunk mindenféle gitárt és hangszedőt, gyakran a zeneszék is váltogatják a gitárjaikat. Mindenre van példa, és ellenpélda is.</p>
