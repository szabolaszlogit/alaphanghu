              <h1>Technikák</h1>
                 
<p>Ismerkedjünk meg néhány olyan technikával, ami riffelés, szólózás közben gyakran előfordul!</p>

<h2>Hammer on (kalapács)</h2>
<p>Fogj le a mutatóujjaddal egy hangot bárhol. Pengesd meg a húrt, majd a gyűrűsujjaddal üss rá kér érintővel távolabb a húrra, ugyanazon a húron. Ez a hammer on, vagy kalapács technika.</p>

<p>Ezt az alapformát, azaz mutatóujjtól két érintővel távolabb kalapácsol a gyűrűsujj, mindenképpen gyakorold be. De vannak más lehetőségek is, ezekkel is találkozhatsz. A gyűrűsujj kalapálhat a harmadik érintőnél, illetve a kisujjat használhatod a harmadik, sőt a negyedik érintőnél is. A kisujj a leggyengébb, legügyetlenebb ujj. Éppen ezért gyakorlod sokat a kalapácsolást, hogy megerősödjön.</p>
<h2>Pull of (visszapengetés)</h2>

<p>A hammer on ellentéte. Csinálj egy kalapácsot, de ne emeld fel a mutató ujjad mikor leteszed a gyűrűsujjad. Ezután a gyűrűsujjaddal pengesd meg a húrt, mégpedig úgy, hogy lehúzod róla, vagy inkább lepattintod, és közben megszólal a húr (amit jelen esetben a mutatóujjad még lenyom két érintővel visszább).</p>
<p>Ezt a technikát is gyakran használjak más ujjakkal (kisujj) és más érintőtávolságokkal. A folyamatos, gyors kalapács majd visszapengetés nagyon jellegzetes a szólókban. Kisujjal szintén sokat kell gyakorolni, amig megügyesedik.</p>
<h2>Slide (csúszás)</h2>


<p>A mutatóujjaddal fogj le egy hangot, pengesd meg, es csúsztasd el az ujjad két érintővel. Magasabb, illetve mélyebb irányba is lehet csúsztatni, tetszőleges számú érintővel, tetszőleges ujjal. A kalapácsnál, visszapengetésnél két külön hang szólal meg, itt viszont hallani lehet az átmenetet is.</p>
<p>
Akár teljes akkordot (jellemzően barrés, vagy kvintes akkordot) is lehet csúsztatni.</p>
<h2>Bend (nyújtás)</h2>
<p>A gyakorlást kezdheted gyűrűsujjal. A lényeg, hogy miután megpengettél egy lefogott hangot, a húrt feltolod. Figyelni kell, hogy egy adott hangközzel szóljon a húr magasabban, egy félhanggal, két félhanggal, három félhanggal.</p>

<p>Úgy gyakorolhatod, hogy megpengeted a lefogott hangot, majd párszor az egy (kettő, három stb.) érintővel magasabb hangot, aztán nyújtáskor megpróbálod ezt a hangmagasságot eltalálni.</p>

<p>Segíthetsz a gyűrűsujjadnak a többivel, szóval akár három ujjal is tolhatod a húrt egyszerre. A magas E húron, a B húron felfelé (a mély E hang felé) tudsz nyújtani, a mély E és A húron pedig lefelé van helyed.</p>

<p>Többféle módon lehet megszólaltatni a hangokat. Lehet maga a nyújtás is hallható (mint a slide-nél), de megpengetheted csak magát a nyújtott hangot is. Miután nyújtottál, megpengetheted a nyújtott húrt, és leengedheted, majd alaphelyzetben újra pengetés. De lehet eleve a nyújtott hanggal kezdeni, ezt pengeted elsőként, aztán visszaengeded. Szóval, sok lehetőség van, a dalokban élnek is ezekkel.</p>
<h2>Vibrato</h2>

<p>A hangot lebegtetjük, az ujjunk mozgatásával. Lehet minden ujjat használni hozzá, leggyakrabban mutató vagy gyűrűsujjal vibrátózunk.</p>

<p>Pengetés után finomam mozgasd az ujjad fel és le (függőlegesen) a hang felengedése nélkül. Fontos érezni a hangmagasságot, ne legyen hamis (a kevesebb néha itt is több), valamint a végén a kiindulóhanghoz térjünk vissza, ne pedig egy eltolt húrral fejezzük be.</p>

<p>Ne csak az ujjad hegye mozogjon. Egyik módszer, hogy az egész kézfejed mozog, közben a gitárt megtámasztod finoman a hüvelyujjaddal, a tenyered élével. De a lényeg, hogy a kézfejed is mozog, nem csak az ujjad ízületei.</p>

<p>Másik módszer, hogy az egész alkarod részt vesz a mozgásban, ez még szabadabb, lebegőbb vibrátót tesz lehetővé.</p>
<p>Egy kis vibrátót gyakran lehet használni, kreatív és improvizatív módon. Sokszor van lehetőség, hogy kicsit lebegtesd a húrt, élj vele! A játékod sokkal élőbb lesz, zamata, íze lesz a melódiának.</p>

<h2>Rolling</h2>

<p>Akkor használjuk, mikor ugyanabban az érintőben, de két húron kell lefogni egymás után a hangokat. Például a D húron az ötödik érintő az első hang, a következő pedig a G húron az ötödik érintő, és mindkettőt mutatóujjal kell lefogni. Ha kipróbálod, láthatod, hogy elég nehéz időben odaérni.</p>

<p>Fogd le az első hangot, szabályosan, ujjheggyel. Pengetés után ujjbeggyel fog le a második hangot, azaz kicsit fektesd le az ujjad. Tulajdonképpen átbillen az ujjad, nem kell mozgatni, felemelni, marad ugyanott. Ezt oda vissza, folytatólagosan lehet gyakorolni.</p>
<p>A fenti technikákat gyakran használjuk, sokszor egymással kombinálva. Mondjuk egy kis vibrátó, majd kalapács, nyújtás, aztán visszapengetés. Rengeteg lehetőség van, a szólókban, riffekben élnek is vele a művészek. Ezért sokszor nehéz lekottázni, vagy elmagyarázni pontosan mit is kellene játszani. Próbáld mindig a dallam hangulatát, karakterét elkapni, ne félj a saját stílusodat használni. Kísérletezz sokat bátran! Nem mindig a pontos másolás a cél, hanem a kreatív dallamalkotás. Jobban szól egy izés riff (még ha kevesebb hang is van benne, mint az eredetiben), mint egy pontos, de élettelen, lelketlen utánzat.</p>


