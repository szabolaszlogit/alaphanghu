              <h1>Riff gyakorlás</h1>
                 
<p>Gyakorolj ki néhány riffet, és ne felejtsd el a gyakorlás legfontosabb szabályát: lassan kell gyakorolni, akkor fog majd gyorsan menni.</p>

<a class="exlink" href="https://www.songsterr.com/a/wsa/the-white-stripes-seven-nation-army-edit-tab-s432573t2" target="_blank" rel="noopener noreferrer">Seven Nation Army (White Stripes)</a>

<p>A refrénben G és A-dúr van, sima lehúzásokkal – csak éppen barrés változatban a harmadik és ötödik érintőnél. A dalban egy másik rész, mikor power chordos E-dúrral játsza le a fenti riffet. Ennél az E-dúrnál a hetedik érintőben van a mutatóujjad, így formáld meg a kvintes akkordoknál tanultak szerint a fogást. Aztán ezt húzogasd végig ugyanúgy, csak már nem egy hangot, hanem az akkordot.</p>
<p>Alkalmazhatod azt a technikát, hogy a mutatóujjad hegye picit hozzáér az E húrhoz, így az nem szól bele az akkordba, ha véletlenül megpengeted.</p>


<a class="exlink" href="https://tabs.ultimate-guitar.com/tab/deep-purple/smoke-on-the-water-tabs-57672" target="_blank" rel="noopener noreferrer">Smoke ont he Water (Deep Purple)</a>

<p>Egy G power chordon alapul. Ha ráérzel a ritmusra, jól fog szólni. Mehet kis vibrato is.</p>

<a class="exlink" href="https://www.songsterr.com/a/wsa/the-rolling-stones-i-cant-get-no-satisfaction-tab-s16453t0" target="_blank" rel="noopener noreferrer">I Cant Get No Satisfaction (The Rolling Stones)</a>

<p>Beizzíthatsz egy fuzz effektet. A hangok nem hosszan, csillapítás nélkül szólnak. Ellenkezőleg, röviden, élesen. Próbáld meg a pengető kezeddel megállítani, lefojtani a hangot pengetés után. Használhatod az a technikát is, hogy a többi húrt lefojtod (mute) a bal kezeddel, de a pengetővel lendületesen, a fojtott húrokat is pengetve játszol. Lejátszhatod csak mutatóujjal is akár, de jobb, ha megpróbálod az ötödik érintőben a kisujjad, a negyedikben pedig a gyűrűsujjad használni.</p>

<a class="exlink" href="https://tabs.ultimate-guitar.com/tab/acdc/highway-to-hell-chords-139966" target="_blank" rel="noopener noreferrer">Highway To Hell (AC/DC)</a>

<p>Remek példa az akkordok kreatív használatára. Kezdetnek az A-dúrt lefoghatod akár egy ujjal is, ráfektetheted a mutatóujjad az húrokra, és csinálj három gyors lehúzást. Tulajdonképpen power chordról van szó, tehát nem baj, ha az alsó három húr fojtva van – de azért pengesd őket, a mély E húrt hüvelykujjal fojthatod le.</p>

<p>Jön egy D, méghozzá úgy, hogy belejátszunk egy F# hangot. Kisujj a B húron, gyűrűs a G húron, a mutató az E húron, közben lefojtja az A húrt.</p>

<p>Ezután a kisujj marad a helyén, de fojthatod vele az E húrt is. A gyűrűsujj nem kell, a középső ujj átmegy a harmadik érintőbe, fojtja közben az A húrt.</p>
<p>A ritmika, a stílus a lényeg ennél a riffnél (is). Ha ez megvan, akár sima D és G akkorddal is jól szól. A dal többi része sem nehéz.</p>


<a class="exlink" href="https://tabs.ultimate-guitar.com/tab/nirvana/come-as-you-are-tabs-47319" target="_blank" rel="noopener noreferrer">Come as you are (Nirvana)</a>

<p>Egyes ujj első érintő, kettes ujj második érintő. Nem nehéz, de jó gyakorolni azt, hogy eltaláld a húrokat a pengetővel és az ujjaiddal.</p>

<a class="exlink" href="https://tabs.ultimate-guitar.com/tab/acdc/thunderstruck-tabs-47698" target="_blank" rel="noopener noreferrer">Thunderstruck (AC/DC)</a>

<p>Általános hiba, hogy ezt a riffet az E húron játszák. Játszák pull-offal (visszapengetéssel) is, ami itt kicsit trükkös. Ugyanis, nem kell pengetned jobb kézzel, hanem csak a bal kezed ujjait használod (mutató és kisujj vagy gyűrűsujj). Nyomd rá az ujjad a húrra, majd kapd le róla, miközben egyúttal pengesd is meg.</p>
<p>De simán mehet visszapengetés nélkül is, használhatod nyugodtan a pengetőt, minden hangot megpengethetsz vele. Az AC/DC is használja mindkét megoldást.</p>


