<p>A gitárt többféleképpen be lehet hangolni, de alapesetben a húrok lefogás nélkül (üresen) a következő hangokon szólalnak meg (a legmélyebb húrtól a legmagasabb felé):</p>

<p class="info">E A D G B E. A két E hang között két oktáv a távolság.</p>

