<h2>A hangok</h2>
<p>A hangokat (beleértve a zeni hangokat is) a frekvenciával (rezgésszámmal) jellemezhetjük, mértékegysége a hertz (Hz). Például a zenei A hang 440 Hz-n szól. Ez tulajdonképpen megegyezésen alapul. Ha megduplázzuk a frekvenciát (vagy megfelezzük) akkor megint egy A hangot kapunk, úgy mondjuk, hogy egy oktávval magasabban, vagy alacsonyabban.</p>

