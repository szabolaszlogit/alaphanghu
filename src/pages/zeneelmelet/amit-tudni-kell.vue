<h2>A hangok nevei</h2>
<p>Egyszerű lenne az életünk, ha ezt a 12 félhangot 12 számmal, vagy az ábécé 12 betűjével jelölnénk. Sajnos azonban fizikai, zeneelméleti és történeti okokból ez nem így van. A 12 félhang neve ábécés hangokkal, úgynevezett abszolút módon a következő:</p>

