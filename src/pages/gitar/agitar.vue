      <h2>Milyen hangszer a gitár?</h2>
      <p>
        A gitár húros hangszer, ezek közül is a pengetős hangszerek csoportjába
        tartozik (azaz kordofon hangszer). Legtöbbször hat húrja van, amit
        kézzel, vagy pengetővel szólaltatunk meg. Vannak több húrral rendelkező
        gitárok is. Gitárszerű hangszerek már az ókorban is léteztek (a név
        eredete is görög, a kithara szóból ered). A 15. századtól kezdett
        kialakulni a mai gitár, a legrégebbi, ma is létező hangszerek a 16.
        századból valók.
      </p>
      <h2>A gitár felépítése</h2>
      <p>
        Alapvetően a gitár három részre tagolódik, fej, tor, potroh. Na, mégsem:
        fej, nyak és test.
        <br />
        A fejen találhatóak a hangolókulcsok. A fej alakja többféle lehet, a
