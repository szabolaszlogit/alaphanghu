        igen könnyű a pontos hangmagasságot eltalálni. Léteznek érintők nélküli
        gitárok is, itt a hangmagasságot folyamatosan, ujjainkat csúsztatva
        lehet változtatni.
        <br />
        A test az akusztikus gitároknál üreges, az itt rezgő levegő szólaltatja
